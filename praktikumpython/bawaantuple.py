# Mendefinisikan tuple
tuple1 = ('p', 'y', 't', 'o', 'n', 's', 'a', 'y', 'a')

# 1. count(x) -> Menghitung jumlah kemunculan item x
# Di sini kita menghitung ada berapa huruf 'a'
# Output: 2
print(tuple1.count('a'))

# 2. index(x) -> Mencari posisi (indeks) pertama dari item x
# Mencari di mana huruf 'n' berada
# Output: 4
print(tuple1.index('n'))