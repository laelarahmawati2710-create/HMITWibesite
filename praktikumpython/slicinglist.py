# Mendefinisikan list
my_list = ['p', 'y', 't', 'h', 'o', 'n', 's', 'a', 'y', 'a']

# 1. Anggota list dari indeks 3 s/d 5 (indeks 6 tidak termasuk)
# Output: ['h', 'o', 'n']
print(my_list[3:6])

# 2. Anggota list dari indeks 4 s/d yang terakhir
# Output: ['o', 'n', 's', 'a', 'y', 'a']
print(my_list[4:])

# 3. Anggota list dari awal (indeks 0) s/d indeks 4 (indeks 5 tidak termasuk)
# Output: ['p', 'y', 't', 'h', 'o']
print(my_list[:5])