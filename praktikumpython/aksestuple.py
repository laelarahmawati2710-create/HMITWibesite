# Mendefinisikan tuple
tuple1 = ('p', 'y', 't', 'h', 'o', 'n')

# 1. Akses indeks 0 (elemen pertama)
# Output: p
print(tuple1[0])

# 2. Akses indeks 1 (elemen kedua)
# Output: y
print(tuple1[1])

# 3. Akses indeks -1 (elemen terakhir)
# Output: n
print(tuple1[-1])

# 4. Akses indeks -2 (elemen kedua dari belakang)
# Output: o
print(tuple1[-2])

