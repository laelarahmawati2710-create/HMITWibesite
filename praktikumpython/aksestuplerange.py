# Mendefinisikan tuple
tuple1 = ('p', 'r', 'o', 'g', 'r', 'a', 'm', 'm', 'i', 'n', 'g')

# 1. Akses dari indeks 0 s/d 2 (indeks 3 tidak termasuk)
# Output: ('p', 'r', 'o')
print(tuple1[:3])

# 2. Akses dari indeks 2 s/d 5 (indeks 6 tidak termasuk)
# Output: ('o', 'g', 'r', 'a')
print(tuple1[2:6])

# 3. Akses dari indeks 3 sampai akhir
# Output: ('g', 'r', 'a', 'm', 'm', 'i', 'n', 'g')
print(tuple1[3:])