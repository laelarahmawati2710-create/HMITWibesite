# Inisialisasi list alfabet yang berantakan
alfabet = ['a', 'b', 'd', 'f', 'e', 'c', 'h', 'g', 'j', 'i']

# 1. Mengurutkan secara Ascending (A-Z)
alfabet.sort()
print(alfabet)
# Output: ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j']

# 2. Mengurutkan secara Descending (Z-A)
alfabet.sort(reverse=True)
print(alfabet)
# Output: ['j', 'i', 'h', 'g', 'f', 'e', 'd', 'c', 'b', 'a']